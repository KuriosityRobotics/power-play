package com.kuriosityrobotics.powerplay.util;

public class Units {
	/**
	 * Constant to convert centimetres to base unit of length (metres)
	 */
	public static double CM = .01;
	public static double M = 1;

	/**
	 * Constant to convert inches to base unit of length (metres)
	 */
	public static double IN = 2.54 * CM;

}
